var numero = prompt ('Digite um número de 1 a 5');

if (numero == 1){
    alert('O número é um.');
}
else if (numero == 2)  {
    alert ('O número é dois.');
}
else if (numero == 3) {
    alert ('O número é três.');
}
else if (numero == 4) {
    alert ('O número é quatro.');
}
else if (numero == 5){
    alert ('O número é cinco.');
}
else {
    alert('Este número não está entre 1 e 5.');
}
    